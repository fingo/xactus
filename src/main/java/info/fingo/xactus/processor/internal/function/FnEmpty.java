package info.fingo.xactus.processor.internal.function;

import java.util.Collection;
import java.util.Iterator;

import info.fingo.xactus.api.ResultSequence;
import info.fingo.xactus.processor.DynamicError;
import info.fingo.xactus.processor.internal.types.QName;
import info.fingo.xactus.processor.internal.types.XSBoolean;

/**
 * If the value of $arg is the empty sequence, the function returns true;
 * otherwise, the function returns false.
 */
public class FnEmpty extends Function {
	/**
	 * Constructor for FnEmpty.
	 */
	public FnEmpty() {
		super(new QName("empty"), 1);
	}

	/**
	 * Evaluate arguments.
	 *
	 * @param args
	 *            argument expressions.
	 * @throws DynamicError
	 *             Dynamic error.
	 * @return Result of evaluation.
	 */
	public ResultSequence evaluate(Collection args, info.fingo.xactus.api.EvaluationContext ec) throws DynamicError {
		return empty(args);
	}

	/**
	 * Empty operation.
	 *
	 * @param args
	 *            Result from the expressions evaluation.
	 * @throws DynamicError
	 *             Dynamic error.
	 * @return Result of fn:empty operation.
	 */
	public static ResultSequence empty(Collection args) throws DynamicError {

		assert args.size() == 1;

		// get args
		Iterator citer = args.iterator();
		ResultSequence arg1 = (ResultSequence) citer.next();

		return XSBoolean.valueOf(arg1.empty());
	}
}
